#!/usr/bin/env python

from __future__ import division
from math import *
from cmath import *

def cosd(x):
  return cos(radians(x))

def sind(x):
  return sin(radians(x))

def tand(x):
  return tan(radians(x))

def acosd(x):
  return degrees(acos(x))

def asind(x):
  return degrees(asin(x))

def atand(x):
  return degrees(atan(x))

def atan2d(y, x):
  return degrees(atan2(y, x))
